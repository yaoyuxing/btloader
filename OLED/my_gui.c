			GUI_VerticalLine(BleWorkStatusEnd);
		break;
		case eBLE_CONNECTED:////已经连接
			OLED_ShowChineseChar(BleWorkStatusStart,Y_START_HEAD,0,CHARACTER_ICON_SIZE,1 ,(int )ChCh_BleIconStatus[1]);
